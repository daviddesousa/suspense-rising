import { useRef, useState, useEffect, useCallback } from 'react';
import './MiniAudioPlayer.css';

/**
 * Bar heights (relative) for the wave pattern — 7 bars.
 * These drive both the idle animation amplitude and the active animation.
 */
const BAR_COUNT = 7;
const BAR_DELAYS = [0, 0.15, 0.05, 0.25, 0.1, 0.2, 0.08]; // stagger offsets

export default function MiniAudioPlayer({ src }) {
  const audioRef = useRef(null);
  const holdTimer = useRef(null);
  const [active, setActive] = useState(false);
  const [showHint, setShowHint] = useState(false);
  const hintTimeout = useRef(null);
  const touchStartTime = useRef(0);

  // ------------------------------------------------------------------
  // Audio control
  // ------------------------------------------------------------------
  const unmute = useCallback(() => {
    if (audioRef.current) {
      audioRef.current.muted = false;
      const playPromise = audioRef.current.play();
      if (playPromise !== undefined) {
        playPromise
          .then(() => {
            setActive(true);
          })
          .catch(() => {
            // Autoplay blocked by browser policy; wait for user interaction
            setActive(false);
          });
      } else {
        setActive(true);
      }
    }
  }, []);

  const mute = useCallback(() => {
    if (audioRef.current) {
      audioRef.current.muted = true;
    }
    setActive(false);
  }, []);

  // ------------------------------------------------------------------
  // Pointer (mouse/trackpad) — hover
  // ------------------------------------------------------------------
  const handlePointerEnter = useCallback(
    (e) => {
      // Only trigger for non-touch pointers
      if (e.pointerType === 'touch') return;
      unmute();
    },
    [unmute],
  );

  const handlePointerLeave = useCallback(
    (e) => {
      if (e.pointerType === 'touch') return;
      mute();
    },
    [mute],
  );

  const handlePointerDown = useCallback(
    (e) => {
      if (e.pointerType === 'touch') return;
      // Allow clicking the player to unmute if hover was blocked by autoplay policy
      unmute();
    },
    [unmute],
  );

  // ------------------------------------------------------------------
  // Touch — long-press to activate, release to deactivate
  // ------------------------------------------------------------------
  const handleTouchStart = useCallback(
    (e) => {
      e.preventDefault(); // prevent ghost clicks & text selection
      touchStartTime.current = Date.now();
      setShowHint(false);
      holdTimer.current = setTimeout(() => {
        unmute();
      }, 200); // slight delay so quick taps don't trigger
    },
    [unmute],
  );

  // Prevent the browser from starting a text-selection drag while the
  // user is holding the player on iOS.
  const handleTouchMove = useCallback((e) => {
    e.preventDefault();
  }, []);

  const handleTouchEnd = useCallback(() => {
    const duration = Date.now() - touchStartTime.current;
    clearTimeout(holdTimer.current);
    mute();

    if (duration < 200) {
      setShowHint(true);
      if (hintTimeout.current) clearTimeout(hintTimeout.current);
      hintTimeout.current = setTimeout(() => {
        setShowHint(false);
      }, 1500);
    }
  }, [mute]);

  // Clean up timers on unmount
  useEffect(() => {
    return () => {
      clearTimeout(holdTimer.current);
      clearTimeout(hintTimeout.current);
    };
  }, []);

  return (
    <div
      className="mini-audio-player"
      onPointerEnter={handlePointerEnter}
      onPointerLeave={handlePointerLeave}
      onPointerDown={handlePointerDown}
      onTouchStart={handleTouchStart}
      onTouchMove={handleTouchMove}
      onTouchEnd={handleTouchEnd}
      onTouchCancel={handleTouchEnd}
      aria-label={
        active ? 'Audio playing' : 'Audio muted — hover or hold to listen'
      }
      role="button"
      tabIndex={0}
    >
      {/* Hidden audio element */}
      {src && <audio ref={audioRef} src={src} loop muted preload="auto" />}

      {/* ── Desktop layout ── */}
      <div className="player-desktop">
        <SpeakerIcon active={active} />
        <div className="wave-bars">
          {Array.from({ length: BAR_COUNT }).map((_, i) => (
            <div
              key={i}
              className={`wave-bar ${active ? 'wave-bar--active' : ''}`}
              style={{
                animationDelay: `${BAR_DELAYS[i]}s`,
                '--bar-index': i,
              }}
            />
          ))}
        </div>
      </div>

      {/* ── Touch layout ── */}
      <div className="player-touch">
        <div className="touch-wave-area">
          <div className="wave-bars wave-bars--touch">
            {Array.from({ length: BAR_COUNT }).map((_, i) => (
              <div
                key={i}
                className={`wave-bar ${active ? 'wave-bar--active' : ''}`}
                style={{
                  animationDelay: `${BAR_DELAYS[i]}s`,
                  '--bar-index': i,
                }}
              />
            ))}
          </div>
        </div>
        <div
          className={`fingerprint-wrap ${active ? 'fingerprint-wrap--active' : ''}`}
        >
          <FingerprintIcon />
          <div className={`hold-hint ${showHint ? 'hold-hint--visible' : ''}`}>
            HOLD
          </div>
        </div>
      </div>
    </div>
  );
}

// --------------------------------------------------------------------------
// Speaker SVG icon — switches between muted/active states
// --------------------------------------------------------------------------
function SpeakerIcon({ active }) {
  return (
    <svg
      className={`speaker-icon ${active ? 'speaker-icon--active' : 'speaker-icon--muted'}`}
      viewBox="0 0 24 24"
      fill="none"
      stroke="currentColor"
      strokeWidth="2"
      strokeLinecap="round"
      strokeLinejoin="round"
      aria-hidden="true"
    >
      {/* Speaker body */}
      <polygon points="11 5 6 9 2 9 2 15 6 15 11 19 11 5" />
      {/* Sound waves — hidden when muted */}
      {active ? (
        <>
          <path d="M15.54 8.46a5 5 0 0 1 0 7.07" />
          <path d="M19.07 4.93a10 10 0 0 1 0 14.14" opacity="0.6" />
        </>
      ) : (
        /* Muted cross */
        <>
          <line x1="23" y1="9" x2="17" y2="15" />
          <line x1="17" y1="9" x2="23" y2="15" />
        </>
      )}
    </svg>
  );
}

